// Get all services directories
const servicesDir = path.join(process.cwd(), 'services');
const services = fs.readdirSync(servicesDir, { withFileTypes: true })
  .filter(dirent => dirent.isDirectory())
  .map(dirent => dirent.name);

console.log('üîß Creating missing UI components for services...');

for (const service of services) {
  const servicePath = path.join(servicesDir, service);
  const uiPath = path.join(servicePath, 'components', 'ui');
  const libPath = path.join(servicePath, 'lib');
  
  try {
    // Create lib directory and utils if it doesn't exist
    if (!fs.existsSync(libPath)) {
      fs.mkdirSync(libPath, { recursive: true });
    }
    
    const utilsPath = path.join(libPath, 'utils.ts');
    if (!fs.existsSync(utilsPath)) {
      fs.writeFileSync(utilsPath, components['utils.ts']);
      console.log(`  ‚úÖ Created lib/utils.ts for ${service}`);
    }
    
    // Create UI components directory
    if (!fs.existsSync(uiPath)) {
      fs.mkdirSync(uiPath, { recursive: true });
    }
    
    // Create all missing UI components
    for (const [filename, content] of Object.entries(components)) {
      if (filename === 'utils.ts') continue; // Already handled above
      
      const componentPath = path.join(uiPath, filename);
      if (!fs.existsSync(componentPath)) {
        fs.writeFileSync(componentPath, content);
        console.log(`  ‚úÖ Created ${filename} for ${service}`);
      }
    }
    
    // Update the index.ts to only export existing components
    const indexPath = path.join(uiPath, 'index.ts');
    const validExports = [
      "export { Button } from './button';",
      "export { Card, CardHeader, CardTitle, CardContent, CardDescription, CardFooter } from './card';",
      "export { Badge } from './badge';",
      "export { Input } from './input';", 
      "export { Label } from './label';",
      "export { Tabs, TabsContent, TabsList, TabsTrigger } from './tabs';",
      "export { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from './select';",
      "export { LoadingSpinner } from './loading-spinner';",
      "export { ErrorMessage } from './error-message';"
    ];
    
    fs.writeFileSync(indexPath, validExports.join('\n') + '\n');
    console.log(`  ‚úÖ Updated index.ts for ${service}`);
    
  } catch (error) {
    console.error(`  ‚ùå Error processing ${service}:`, error.message);
  }
}

console.log('‚úÖ UI components creation completed!');
